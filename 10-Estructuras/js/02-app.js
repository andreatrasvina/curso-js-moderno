const puntaje = 1000;

if(puntaje != 1000) {
    console.log('si es diferente');
} 

//operador estricto
if(puntaje === '1000') {
    console.log('si es igual');
} else {
    console.log('no es igual');
}

//estricto de diferente, revisa el valor y el tipo de dato de la variable
if(puntaje !== '1000') {
    console.log('si es igual');
}