const num1 = 30;
const num2 = 20;

let resultado;

//Suma
resultado = num1 + num2;
console.log(resultado);

//Resta
resultado = num1 - num2;
console.log(resultado);

//Multiplicacion
resultado = num1 * num2;
console.log(resultado);

//Division
resultado = num1 / num2;
console.log(resultado);

//Modulo
resultado = num1 % num2;
console.log(resultado);