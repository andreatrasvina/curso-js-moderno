//CREACION DE NUMEROS, en js todos los tipos de numeros se crean de la misma manera 

//entero
const num1 = 30;
console.log(num1);

const num2 = 20;
console.log(num2);

//flotante
const num3 = 20.20;
console.log(num3);

//fraccion
